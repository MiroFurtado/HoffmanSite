<h1>Harvard Undergraduates</h1>

<h3>Information Requested</h3>
<ul>
 <li>Send to Prof. Hoffman (<a href="mailto:jhoffman@physics.harvard.edu?subject=work%20in%20%your%20lab&cc=dlclarke@fas.harvard.edu">jhoffman&#64;physics.harvard.edu</a>) and cc to Dionne Clarke (<a 
href="mailto:dlclarke@fas.harvard.edu">dlclarke&#64;fas.harvard.edu</a>) with useful subject line.</li>
 <li>Email about your experience, interests, and goals (1-2 paragraphs. No prior research experience is necessary, but if you have some, we'd be interested to hear about it.)</li>
 <li>CV (include experience, skills, and coursework; see <a href="exampleCV1.pdf">example</a>)</li>
 <li>Unofficial transcript (not essential, if relevant coursework is already on your CV)</li>
 <li>Preferred employment dates (e.g. &quot;spring semester only&quot; or &quot;summer from June 10-Aug 20, then continuing into fall semester &amp; possibly beyond&quot;. Note that a summer internship is 
typically 10+ weeks, and a term-time commitment must be at least 10 hours/week, to maintain substantive progress.)</li>
 <li>Funding you have in hand, or plan to apply for (see below); or desired course credit (e.g. ES 91r, Physics 91r). Note that you can <em>either</em> receive pay or course credit in a 
given semester, but not both simultaneously.</li>
 </ul>

